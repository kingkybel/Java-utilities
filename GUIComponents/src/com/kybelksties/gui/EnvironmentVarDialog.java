    private void categoryComboActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_categoryComboActionPerformed
    {//GEN-HEADEREND:event_categoryComboActionPerformed
        Object item = categoryCombo.getEditor().getItem();
        ComboBoxModel model = categoryCombo.getModel();
        boolean found = false;
        for (int i = 0; i < model.getSize() && !found; i++)
        {
            if (model.getElementAt(i).equals(item))
            {
                found = true;
            }
        }
        if (!found)
        {

        }
    }//GEN-LAST:event_categoryComboActionPerformed

