    private void okButtonActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_okButtonActionPerformed
    {//GEN-HEADEREND:event_okButtonActionPerformed
        PodVariant.Type type = (PodVariant.Type) typeComboBox.getSelectedItem();
        Object value =
               type.equals(PodVariant.Type.BOOLEAN) ? booleanCheck.isSelected() :
               type.equals(PodVariant.Type.DOUBLE) ? intSpinner.getValue() :
               type.equals(PodVariant.Type.INTEGER) ? intSpinner.getValue() :
               type.equals(PodVariant.Type.STRING) ? textPane.getText() :
               null;
        String category = categoryCombo.getSelectedItem().toString();
        if (value != null)
        {
            theValue = new EnvironmentVar(type,
                                          category,
                                          varNameInput.getText(),
                                          true,
                                          new PodVariant(value));
        }
        setVisible(false);
    }//GEN-LAST:event_okButtonActionPerformed

    private void typeComboBoxActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_typeComboBoxActionPerformed
    {//GEN-HEADEREND:event_typeComboBoxActionPerformed
        EnvironmentVar.StereoType type =
                                  (EnvironmentVar.StereoType) typeComboBox.
                                  getSelectedItem();
        CardLayout cardLayout = (CardLayout) (valueControlPanel.getLayout());
        String cardName =
               type.equals(PodVariant.Type.BOOLEAN) ? "BOOLEAN_VALUE" :
               type.equals(PodVariant.Type.DOUBLE) ? "DOUBLE_VALUE" :
               type.equals(PodVariant.Type.INTEGER) ? "INT_VALUE" :
               type.equals(PodVariant.Type.STRING) ? "TEXT_VALUE" :
               "BOOLEAN_VALUE";
        cardLayout.show(valueControlPanel, cardName);

    }//GEN-LAST:event_typeComboBoxActionPerformed

